    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            String course_name = txtcname.getText();
            String total_marks = txttmarks.getText();
            String duration = txtduration.getText();
            String course_teacher = txtcteacher.getText();
            
            pst = con.prepareStatement("insert into course(course_name,total_marks,duration,course_teacher)values(?,?,?,?)");
            pst.setString(1,course_name);
            pst.setString(2,total_marks);
            pst.setString(3,duration);
            pst.setString(4,course_teacher);
            int k = pst.executeUpdate();
            
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "Record added");
                txtcname.setText("");
                txttmarks.setText("");
                txtduration.setText("");
                txtcteacher.setText("");
                txtcname.requestFocus();
                Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Failed to add record");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            DefaultTableModel d = (DefaultTableModel)jTable2.getModel();
            
            int selectIndex = jTable2.getSelectedRow();
            
            String id = d.getValueAt(selectIndex, 0).toString();
            
            String course_name = txtcname.getText();
            String total_marks = txttmarks.getText();
            String duration = txtduration.getText();
            String course_teacher = txtcteacher.getText();
            
            pst = con.prepareStatement("update course set course_name=?,total_marks=?,duration=?,course_teacher=? where id=?");
            pst.setString(1,course_name);
            pst.setString(2,total_marks);
            pst.setString(3,duration);
            pst.setString(4,course_teacher);
            pst.setString(5,id);
            int k = pst.executeUpdate();
            
            if(k==1)
            {
                JOptionPane.showMessageDialog(this, "Record is successfully updated");
                txtcname.setText("");
                txttmarks.setText("");
                txtduration.setText("");
                txtcteacher.setText("");
                txtcname.requestFocus();
                jButton1.setEnabled(true);
                Load();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Failed to update record");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

