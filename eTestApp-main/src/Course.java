    public void Load(){
        
        try{
            pst = con.prepareStatement("select * from course");
            rs = pst.executeQuery();
            
            ResultSetMetaData rsd = rs.getMetaData();
            int c = rsd.getColumnCount();
            DefaultTableModel d = (DefaultTableModel)jTable2.getModel();
            d.setRowCount(0);
            
            while(rs.next())
            {
                Vector v = new Vector();
                for(int i=0; i<=c; i++){
                    v.add(rs.getString("id"));
                    v.add(rs.getString("course_name"));
                    v.add(rs.getString("total_marks"));
                    v.add(rs.getString("duration"));
                    v.add(rs.getString("course_teacher"));
                }
                d.addRow(v);
            }
            
        } 
        catch(SQLException ex){
            Logger.getLogger(Student.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
