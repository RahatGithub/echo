    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String username = txtuser.getText();
        String pass = txtpass.getText();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = (Connection) (java.sql.Connection) java.sql.DriverManager.getConnection("jdbc:mysql://localhost/etest","root","");
            pst = (PreparedStatement) con.prepareStatement("select * from student where name=? and code_no=?");
            pst.setString(1,username);
            pst.setString(2,pass);
            ResultSet rs = (ResultSet) pst.executeQuery();

            if(rs.next()){
                if(txtuser.getText().equals("admin")){
                    this.hide();
                    Student a = new Student();
                    a.setVisible(true);
                }
                else{
                    this.hide();
                    Test a = new Test(txtuser.getText());
                    a.setVisible(true);
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"UserName or Password do not Match");
                txtuser.setText("");
                txtpass.setText("");
                txtuser.requestFocus();
            }

        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1MouseClicked

